# (+ Operators)
x = 1
y = 2
z = 3.0
result = x + y + z
print('+ Operators:', result)


# Concatenate Two String.
first_name = 'Salman'
last_name = 'Pasha'

name = first_name + ' ' + last_name
print("Name:", name)


# (- Operators)
a = 12
b = 9
result = a - b
print('-Operators:', result)


# (* Operators)
a = 12
b = 9
result = a * b
print('*Operators:', result)

# Multiplying String.
a = 'salman'
b = a*3
print(b)


# / and // Operators.
result = 14.7/3
print(result)

result = 14.7//3
print(result)


# (% Operators).
result = 13%5
print('% Operators:',result)


# (** Operators)
result = 13**5
print('** Operators:',result)









